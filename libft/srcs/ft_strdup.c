#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*c;
	int		i;

	i = 0;
	while (s1[i])
		i++;
	if (!(c = (char*)malloc(sizeof(char) * (i + 1))))
		return (0);
	i = 0;
	while (s1[i])
	{
		c[i] = s1[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
